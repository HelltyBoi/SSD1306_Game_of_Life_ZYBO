int InitIIC(u16 DeviceId){

	int Status;
	XIicPs_Config *Config;

	Config = XIicPs_LookupConfig(DeviceId);
	if (NULL == Config) {
		xil_printf("LookupConfig failed\n\r");
		return FAILURE;
	}


	Status = XIicPs_CfgInitialize(&Iic, Config, Config->BaseAddress);
	if (Status != XST_SUCCESS) {
		xil_printf("CfgInitialize failed\n\r");
		return XST_FAILURE;
	}


	Status = XIicPs_SelfTest(&Iic);
	if (Status != XST_SUCCESS) {
		xil_printf("SelfTest failed\n\r");
		return XST_FAILURE;
	}


	XIicPs_SetSClk(&Iic, IIC_SCLK_RATE);

	return SUCCESS;
}


int InitSSD1306(void){

	int Status;

	u8 SendBuffer[]={

			0xAE,0xDA,0x80,0xA8,0x3F,0xD3,
			0x00,0x40,0x8D,0x14,0xA1,0xC8,
			0xDA,0x12,0x81,0xCF,0xD9,0xF1,
			0xDB,0x40,0xA4,0xA6,0x21,0x00,
			0x7F,0x22,0x00,0x07,0x20,0x00,
			0xAF,
	};

	Status = XIicPs_MasterSendPolled(&Iic, SendBuffer,
			(sizeof(SendBuffer)/sizeof(SendBuffer[0])), 0x3C);
	if (Status != XST_SUCCESS) {
		xil_printf("MasterSendPolled failed\n\r");
		return XST_FAILURE;
	}
	return SUCCESS;
}

int Draw128x64(u8 *DrawBuffer){
	int Status,i=0;
	u8 Buffer[1025];
	Buffer[0]=0x40;

	for(i=1; i<1025; ++i){
		Buffer[i]=*(DrawBuffer+i-1);
	}

	Draw(Buffer, sizeof(Buffer)/sizeof(Buffer[0]));
	return XST_SUCCESS;
}

int Draw(u8 *DrawBuffer, u32 DrawBufferSize){

	int Status;

	Status = XIicPs_MasterSendPolled(&Iic, DrawBuffer,
			DrawBufferSize, SLAVE_ADDR);
	if (Status != XST_SUCCESS) {
		xil_printf("MasterSendPolled failed\n\r");
		return XST_FAILURE;
	}

	return XST_SUCCESS;
}

void writeSSD1306(char *chr){
	int intchr;
	int i=1;
	u8 array[6];

	Clear[0]=0x40;
	Draw(Clear,1025);

	while(*chr!=0){
		intchr=(int)*chr;
		intchr=intchr-32;

		array[0]=0x40;
		for(i=1; i<6; i++)
			array[i]=Characters[5*intchr+i-1];

		if(*chr!='\n'){
			Draw(array,6);
		}
		chr++;
	}

}

